CREATE TABLE EXECUTION_JOB (
  JOB_ID VARCHAR2(100) NOT NULL,
  INTENT_ID NUMBER(19) NOT NULL,
  IDEMPOTENCY_KEY VARCHAR2(200) NOT NULL,
  ANALYSIS_TYPE VARCHAR2(40) NOT NULL,
  EXECUTION_STATE VARCHAR2(40) NOT NULL,
  INPUT_SNAPSHOT CLOB NOT NULL,
  FAILURE_REASON CLOB,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT PK_EXECUTION_JOB PRIMARY KEY (JOB_ID),
  CONSTRAINT UK_EXECUTION_JOB_IDEMPOTENCY UNIQUE (IDEMPOTENCY_KEY),
  CONSTRAINT CK_EXECUTION_JOB_TYPE CHECK (ANALYSIS_TYPE IN ('REVIEWER_ASSIST', 'CONFLICT_ANALYSIS', 'SCREENING')),
  CONSTRAINT CK_EXECUTION_JOB_STATE CHECK (
    EXECUTION_STATE IN (
      'QUEUED',
      'DISPATCHED',
      'RUNNING',
      'SUCCEEDED',
      'FAILED_RETRYABLE',
      'FAILED_TERMINAL',
      'DEAD_LETTERED'
    )
  )
);

CREATE TABLE EXECUTION_ATTEMPT (
  ATTEMPT_ID NUMBER(19) NOT NULL,
  JOB_ID VARCHAR2(100) NOT NULL,
  ATTEMPT_NO NUMBER(10) NOT NULL,
  ATTEMPT_STATE VARCHAR2(40) NOT NULL,
  PROVIDER_NAME VARCHAR2(80),
  STARTED_AT TIMESTAMP,
  FINISHED_AT TIMESTAMP,
  ERROR_MESSAGE CLOB,
  CONSTRAINT PK_EXECUTION_ATTEMPT PRIMARY KEY (ATTEMPT_ID),
  CONSTRAINT UK_EXECUTION_ATTEMPT UNIQUE (JOB_ID, ATTEMPT_NO),
  CONSTRAINT CK_EXECUTION_ATTEMPT_STATE CHECK (
    ATTEMPT_STATE IN ('STARTED', 'SUCCEEDED', 'FAILED_RETRYABLE', 'FAILED_TERMINAL')
  )
);

CREATE TABLE EXECUTION_ARTIFACT (
