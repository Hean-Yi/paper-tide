CREATE TABLE ANALYSIS_INTENT (
  INTENT_ID NUMBER(19) NOT NULL,
  ANALYSIS_TYPE VARCHAR2(40) NOT NULL,
  BUSINESS_ANCHOR_TYPE VARCHAR2(30) NOT NULL,
  BUSINESS_ANCHOR_ID NUMBER(19) NOT NULL,
  REQUESTED_BY NUMBER(19) NOT NULL,
  IDEMPOTENCY_KEY VARCHAR2(200) NOT NULL,
  BUSINESS_STATUS VARCHAR2(30) NOT NULL,
  EXECUTION_JOB_ID VARCHAR2(100),
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT PK_ANALYSIS_INTENT PRIMARY KEY (INTENT_ID),
  CONSTRAINT UK_ANALYSIS_INTENT_IDEMPOTENCY UNIQUE (IDEMPOTENCY_KEY),
  CONSTRAINT CK_ANALYSIS_INTENT_TYPE CHECK (ANALYSIS_TYPE IN ('REVIEWER_ASSIST', 'CONFLICT_ANALYSIS', 'SCREENING')),
  CONSTRAINT CK_ANALYSIS_INTENT_ANCHOR CHECK (BUSINESS_ANCHOR_TYPE IN ('ASSIGNMENT', 'ROUND', 'MANUSCRIPT')),
  CONSTRAINT CK_ANALYSIS_INTENT_STATUS CHECK (BUSINESS_STATUS IN ('REQUESTED', 'AVAILABLE', 'FAILED_VISIBLE', 'SUPERSEDED'))
);

CREATE TABLE ANALYSIS_PROJECTION (
  PROJECTION_ID NUMBER(19) NOT NULL,
  INTENT_ID NUMBER(19) NOT NULL,
  ANALYSIS_TYPE VARCHAR2(40) NOT NULL,
  VISIBILITY_LEVEL VARCHAR2(30) NOT NULL,
  BUSINESS_STATUS VARCHAR2(30) NOT NULL,
  SUMMARY_TEXT CLOB,
  REDACTED_RESULT CLOB,
  RAW_RESULT_REFERENCE VARCHAR2(100),
  IS_SUPERSEDED NUMBER(1) DEFAULT 0 NOT NULL,
  UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT PK_ANALYSIS_PROJECTION PRIMARY KEY (PROJECTION_ID),
  CONSTRAINT UK_ANALYSIS_PROJECTION_INTENT UNIQUE (INTENT_ID),
  CONSTRAINT CK_ANALYSIS_PROJECTION_TYPE CHECK (ANALYSIS_TYPE IN ('REVIEWER_ASSIST', 'CONFLICT_ANALYSIS', 'SCREENING')),
  CONSTRAINT CK_ANALYSIS_PROJECTION_VISIBILITY CHECK (VISIBILITY_LEVEL IN ('NONE', 'REDACTED_ONLY', 'RAW_AND_REDACTED')),
  CONSTRAINT CK_ANALYSIS_PROJECTION_STATUS CHECK (BUSINESS_STATUS IN ('REQUESTED', 'AVAILABLE', 'FAILED_VISIBLE', 'SUPERSEDED')),
  CONSTRAINT CK_ANALYSIS_PROJECTION_SUPERSEDED CHECK (IS_SUPERSEDED IN (0, 1))
);

CREATE TABLE ANALYSIS_OUTBOX (
  OUTBOX_ID NUMBER(19) NOT NULL,
  INTENT_ID NUMBER(19) NOT NULL,
  MESSAGE_TYPE VARCHAR2(80) NOT NULL,
  MESSAGE_KEY VARCHAR2(200) NOT NULL,
  MESSAGE_PAYLOAD CLOB NOT NULL,
  MESSAGE_STATUS VARCHAR2(20) NOT NULL,
  RETRY_COUNT NUMBER(10) DEFAULT 0 NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  PUBLISHED_AT TIMESTAMP,
  CONSTRAINT PK_ANALYSIS_OUTBOX PRIMARY KEY (OUTBOX_ID),
  CONSTRAINT UK_ANALYSIS_OUTBOX_KEY UNIQUE (MESSAGE_KEY),
  CONSTRAINT CK_ANALYSIS_OUTBOX_STATUS CHECK (MESSAGE_STATUS IN ('PENDING', 'PUBLISHED', 'FAILED'))
);

CREATE TABLE ANALYSIS_INBOX (
  INBOX_ID NUMBER(19) NOT NULL,
