  PROCESSED_AT TIMESTAMP,
  CONSTRAINT PK_ANALYSIS_INBOX PRIMARY KEY (INBOX_ID),
  CONSTRAINT UK_ANALYSIS_INBOX_KEY UNIQUE (MESSAGE_KEY),
  CONSTRAINT CK_ANALYSIS_INBOX_STATUS CHECK (PROCESS_STATUS IN ('PENDING', 'PROCESSED', 'FAILED'))
